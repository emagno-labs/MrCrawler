CREATE TABLE users (
   id INTEGER NOT NULL,
   oauth_token VARCHAR(50),
   oauth_token_secret VARCHAR(50),
   created DATETIME,
   last_access DATETIME,
   browser VARCHAR(50),
   is_temporary BOOLEAN,
   id_twitter INTEGER,
   name VARCHAR(50),
   screen_name VARCHAR(50),
   description TEXT,
   lang VARCHAR(10),
   time_zone VARCHAR(50),
   location VARCHAR(100),
   geo_enabled BOOLEAN,
   url VARCHAR(100),
   profile_image_url VARCHAR(250),
   friends_count INTEGER,
   statuses_count INTEGER,
   favourites_count INTEGER,
   PRIMARY KEY (id),
   UNIQUE (oauth_token),
   UNIQUE (oauth_token_secret),
   CHECK (is_temporary IN (0, 1)),
   CHECK (geo_enabled IN (0, 1))
)
